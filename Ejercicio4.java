/**
 * This program displays the numbers from 320 to 160, counting backwards 20 by 20 using a for loop.
 * 
 * @autor Marina Ruiz
 */

public class Ejercicio4 {
    public static void main(String args[]) {
        for (int n = 320; n >= 160; n -= 20) {
            System.out.println(n);
        }
    }
}
