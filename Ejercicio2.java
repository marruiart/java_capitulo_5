/**
 * This program displays the numbers that are multiples of 5 from 0 to 100 using
 * a while loop.
 * 
 * @autor Marina Ruiz
 */

public class Ejercicio2 {
    public static void main(String args[]) {
        int n = 0;
        while (n <= 100) {
            System.out.println(n);
            n += 5;
        }
    }
}
