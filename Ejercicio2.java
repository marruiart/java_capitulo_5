/**
 * Este programa muestra los números múltiplos de 5 de 0 a 100 utilizando un
 * bucle while.
 * 
 * @autor Marina Ruiz
 */

public class Ejercicio2 {
    public static void main(String args[]) {
        int n = 0;
        while (n <= 100) {
            System.out.println(n);
            n += 5;
        }
    }
}
