/**
 * Este programa muestra los números múltiplos de 5 de 0 a 100 utilizando un
 * bucle for.
 * 
 * @autor Marina Ruiz
 */

public class Ejercicio1 {
    public static void main(String args[]) {
        for (int n = 0; n <= 100; n += 5) {
            System.out.printf("%d  ", n);
        }
    }
}
